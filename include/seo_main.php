<p align="justify">Компания располагает сетью филиалов на всей территории России, каждый филиал осуществляет поставку техники, ввод в эксплуатацию, сервисное обслуживание (стационарное и выездное), поставку запасных частей. В автопарке головного офиса и филиалов имеется 200 мобильных мастерских, в том числе повышенной проходимости. На нашем складе постоянно в наличии более 1500 единиц техники и свыше 20 000 наименований запчастей. Компания располагает сетью из 40 филиалов по всей территории России.</p>
 
